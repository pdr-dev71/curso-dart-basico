import 'dart:io';

void main() {
  //Operadores atribuição (binário/infix)
  double a = 3;
  a = a + 3;
  a += 3;
  a -= 5;
  a *= 3;
  a /= 3;
  print(a);

  //Operadores relacionai = Resultado sempre é um valor booleano
  print(3 > 2);
  print(4 < 8);
  print(5 >= 5);
  print(6 != 5);
  print(3 == 3);
  print(4 == '4');
  
}
